@Composable
fun AppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit,
) {
    MaterialTheme(
        colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme,
        typography = AccessTypography,
    ) {
        // splash 사라진 직후 NavHost/Scaffold 가 measure 되는 한두 frame 동안 window background 가 그대로
        // 노출되어 "팝" 하고 화면이 나타나는 깜빡임이 보인다. 풀스크린 Surface 를 깔아 첫 frame 부터
        // colorScheme.background 가 채워지도록 보장 → blank frame 제거.
        Surface(
            modifier = Modifier.fillMaxSize(),
            color = MaterialTheme.colorScheme.background,
            content = content,
        )
    }
}
